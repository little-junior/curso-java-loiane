package com.liljon.curso_java.aula11;

public class VariaveisInteiras {
    public static void main(String[] args) {
        int total = 20;
        byte pequeno = 120;
        short maior = 1000;
        long grandao = 1000000000;

        System.out.println("inteiro: " + total);
        System.out.println("byte: " + pequeno);
        System.out.println("short: " + maior);
        System.out.println("long: " + grandao);

    }
}
